    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int ID = Integer.parseInt(txtID.getText());
        String Tenphong = txtTenphong.getText();
        String Giaphong = txtGiaphong.getText();
        String Mota = txtMota.getText();
        String Hinhanh = txtHinhanh.getText();
        String Loaisp = cbLoaisp.getSelectedItem().toString();
        String IDsanpham; 
        if (Loaisp == "Nhà trọ giá rẻ"){
            IDsanpham="1";
        }
        else if (Loaisp == "Ký túc xá giá rẻ"){
            IDsanpham="2";
        }else if (Loaisp == "Hotel"){
            IDsanpham="3";
        }else{
            IDsanpham="4";
        }
        Phong p = new Phong(ID,Tenphong,Giaphong,Hinhanh,Mota,IDsanpham);
        
        PhongModify md = new javalesson2.PhongModify();
        md.insert(p);
        showPhong();
        
    }//GEN-LAST:event_btnSaveActionPerformed

