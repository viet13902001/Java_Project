    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblPhong.getSelectedRow();
        if (selectedIndex >=0){

            Phong phong = phongList.get(selectedIndex);
            
            int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa phòng?", "Comfirm", JOptionPane.YES_NO_OPTION);
            System.out.println("Option: " +option);
            
            if (option == 0){
                PhongModify md = new javalesson2.PhongModify();
                md.delete(phong.getID());
                showPhong();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

