    private void showPhong() {
        
        PhongModify md = new javalesson2.PhongModify();
//        ArrayList<Phong> phongList = new ArrayList<>();
        try {
            phongList = md.findAll();
        } catch (SQLException ex) {
            Logger.getLogger(PhongModify.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tableModel.setRowCount(0);
        String str []= {"Nhà trọ giá rẻ","Ký túc xá giá rẻ","Hotel","Homestay"};

        
        phongList.forEach((phong) -> {
            tableModel.addRow(new Object[] {phong.getID(), phong.getTensanpham(),
                                phong.getGiasanpham(),phong.getHinhanhsanpham(),
                                phong.getMotasanpham(),str[Integer.parseInt(phong.getIDsanpham())-1]});
        });
    }

